# String: Cadena de texto
nombre = "Erick"
print(nombre)
# Integer: Entero
edad = 24
print(edad)
# Float: Decimal
decimal = 10.5
print(decimal)
# Boolean: Verdadero o Falso
verdadero = True
print(verdadero)
# List: Lista
lista = ["Erick", 24, 10.5, True]
print(lista)
# Tuple: Tupla
tupla = ("Erick", 24, 10.5, True)
print(tupla)
# Dictionary: Diccionario
diccionario = {"nombre": "Erick", "edad": 24, "decimal": 10.5, "verdadero": True}
print(diccionario)


# Convertir variables
# Casteo: transformar una variable de un tipo a otro
# Integer
edad = "24"
print(edad)
edad = int(edad)
print(edad)
# Float
edad = float(edad)
print(edad)

# ver tipo de variable
# type()
print(type(edad))
print(type(nombre))